public class Human {
    public String nama;
    public String ras;

    public Human(String nama, String ras){
        this.nama = nama;
        this.ras = ras;
    }

    public String getNama() {
        return this.nama;
    }
    public String getRas() {
        return this.ras;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
    public void setRas(String ras) {
        this.ras = ras;
    }
}
